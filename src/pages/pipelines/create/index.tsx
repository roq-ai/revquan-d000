import {
  FormControl,
  FormLabel,
  Input,
  Button,
  Text,
  Box,
  Spinner,
  FormErrorMessage,
  Switch,
  Flex,
} from '@chakra-ui/react';
import Breadcrumbs from 'components/breadcrumb';
import DatePicker from 'components/date-picker';
import { Error } from 'components/error';
import { FormWrapper } from 'components/form-wrapper';
import { NumberInput } from 'components/number-input';
import { SelectInput } from 'components/select-input';
import { AsyncSelect } from 'components/async-select';
import { TextInput } from 'components/text-input';
import AppLayout from 'layout/app-layout';
import { FormikHelpers, useFormik } from 'formik';
import { useRouter } from 'next/router';
import { FunctionComponent, useState } from 'react';
import * as yup from 'yup';
import { AccessOperationEnum, AccessServiceEnum, requireNextAuth, withAuthorization } from '@roq/nextjs';
import { compose } from 'lib/compose';
import { useRoqClient } from 'lib/roq';
import * as RoqTypes from 'lib/roq/types';

import { pipelineValidationSchema } from 'validationSchema/pipelines';
import { PipelineInterface } from 'interfaces/pipeline';

function PipelineCreatePage() {
  const router = useRouter();
  const [error, setError] = useState(null);
  const roqClient = useRoqClient();
  const handleSubmit = async (values: PipelineInterface, { resetForm }: FormikHelpers<any>) => {
    setError(null);
    try {
      await roqClient.pipeline.create({ data: values as RoqTypes.pipeline });
      resetForm();
      router.push('/pipelines');
    } catch (error) {
      setError(error);
    }
  };

  const formik = useFormik<PipelineInterface>({
    initialValues: {
      expected_close_date: new Date(new Date().toDateString()),
      progress_status: '',
    },
    validationSchema: pipelineValidationSchema,
    onSubmit: handleSubmit,
    enableReinitialize: true,
    validateOnChange: false,
    validateOnBlur: false,
  });

  return (
    <AppLayout
      breadcrumbs={
        <Breadcrumbs
          items={[
            {
              label: 'Pipelines',
              link: '/pipelines',
            },
            {
              label: 'Create Pipeline',
              isCurrent: true,
            },
          ]}
        />
      }
    >
      <Box rounded="md">
        <Box mb={4}>
          <Text as="h1" fontSize={{ base: '1.5rem', md: '1.875rem' }} fontWeight="bold" color="base.content">
            Create Pipeline
          </Text>
        </Box>
        {error && (
          <Box mb={4}>
            <Error error={error} />
          </Box>
        )}
        <FormWrapper onSubmit={formik.handleSubmit}>
          <FormControl id="expected_close_date" mb="4">
            <FormLabel fontSize="1rem" fontWeight={600}>
              Expected Close Date
            </FormLabel>
            <DatePicker
              selected={formik.values?.expected_close_date ? new Date(formik.values?.expected_close_date) : null}
              onChange={(value: Date) => formik.setFieldValue('expected_close_date', value)}
            />
          </FormControl>

          <TextInput
            error={formik.errors.progress_status}
            label={'Progress Status'}
            props={{
              name: 'progress_status',
              placeholder: 'Progress Status',
              value: formik.values?.progress_status,
              onChange: formik.handleChange,
            }}
          />

          <Flex justifyContent={'flex-start'}>
            <Button
              isDisabled={formik?.isSubmitting}
              bg="state.info.main"
              color="base.100"
              type="submit"
              display="flex"
              height="2.5rem"
              padding="0rem 1rem"
              justifyContent="center"
              alignItems="center"
              gap="0.5rem"
              mr="4"
              _hover={{
                bg: 'state.info.main',
                color: 'base.100',
              }}
            >
              Submit
            </Button>
            <Button
              bg="neutral.transparent"
              color="neutral.main"
              type="button"
              display="flex"
              height="2.5rem"
              padding="0rem 1rem"
              justifyContent="center"
              alignItems="center"
              gap="0.5rem"
              mr="4"
              onClick={() => router.push('/pipelines')}
              _hover={{
                bg: 'neutral.transparent',
                color: 'neutral.main',
              }}
            >
              Cancel
            </Button>
          </Flex>
        </FormWrapper>
      </Box>
    </AppLayout>
  );
}

export default compose(
  requireNextAuth({
    redirectTo: '/',
  }),
  withAuthorization({
    service: AccessServiceEnum.PROJECT,
    entity: 'pipeline',
    operation: AccessOperationEnum.CREATE,
  }),
)(PipelineCreatePage);
